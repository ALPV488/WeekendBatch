
public class TypeCastingEx {
	
	public static void main(String[] args) {
	
		
		//Implicit typecasting ---->It's automatic
				int i=10;			//4
				
				long l = i;		//8
		
				System.out.println(l);
				
				
			//Explicit TypeCasting------>
				
				float a = 3.14f;
				
				int b = (int)a;
				
				long c= 398749348;
				
				byte d = (byte)c;		//-128->127
				
				System.out.println(d);
		
				
		
	}
	
}
